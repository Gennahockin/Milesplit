
public class Runner
	{
		private int time;
		private String name;

	public Runner(String n, int t )
			{
			time = t;
			name = n;
			}

		public String getName()
			{
			return name;
			}

		public void setName(String name)
			{
			this.name = name;
			
			}
		public int getTime()
			{
			return time;
			}

		public void setTime(int time)
			{
			this.time = time;
			}

		}


	
